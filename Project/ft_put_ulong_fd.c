#include "libft.h"

void	ft_put_ulong_fd(unsigned long n, int fd)
{
	if (n > 9)
	{
		ft_put_ulong_fd((n / 10),fd);
	}
	ft_putchar_fd(((n % 10) + '0'), fd);
}
